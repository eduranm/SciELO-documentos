<p>Los procedimientos de evaluación comprenden tres aspectos y fases:</p>

<ol type="I">
    <li>Evaluación de la calidad editorial de la publicación,</li>
    <li>Evaluación de contenidos, y</li>
    <li>Evaluación de la sustentabilidad técnica y financiera para la edición electrónica de la revista en la colección SciELO-México.</li>
</ol>

<ol type="I">
    <li><i>Evaluación de calidad editorial (evaluación de aspectos formales de edición):</i><br><br>
        Se evalúa el cumplimiento de los estándares internacionales de edición de revistas científicas. La evaluación de los aspectos formales incluye el análisis de un conjunto de características, basadas en normas internacionales relacionadas con la edición de revistas científicas.
        <br><br>
        Requisitos obligatorios:
        <br><br>
        <ol>
            <li>
                <b>ISSN.</b> Contar con número de ISSN (International Standard Serial Number).
                <br><br>
            </li>
            <li>
                <b>Membrete bibliográfico en cubiertas o páginas de presentación.</b> Entiéndase por membrete un breve resumen de la referencia bibliográfica de la revista, el cual debe comprender los siguientes elementos: título de la revista —abreviado si es largo—, año, volumen, número, páginas e ISSN.
                <br><br>
            </li>
            <li>
                <b>Antigüedad.</b> La revista deberá tener una antigüedad mínima de dos años.
                <br><br>
            </li>
            <li>
                <b>Datos del organismo responsable y lugar de edición:</b>
                <br><br>
                <ul style="margin-left: 25px">
                    <li>
                        <i>Lugar de edición:</i> información completa sobre el lugar de edición de la revista.
                        <br><br>
                    </li>
                    <li>
                        <i>Entidad editora:</i> información sobre la(s) entidad(es) o institución(es) editora(s) de la revista.
                        <br><br>
                    </li>
                    <li>
                        <i>Mención del director:</i> nombre del director de la publicación, responsable editorial o equivalente.
                        <br><br>
                    </li>
                    <li>
                        <i>Mención de la dirección:</i> proporcionar la dirección postal y de correo electrónico de la administración de la revista a efectos de solicitud de suscripciones, canjes, envío de trabajos, etc.
                        <br><br>
                    </li>
                </ul>
            </li>
            <li>
                <b>Mención de periodicidad.</b> Manifestar la periodicidad (frecuencia de publicación) de la revista: bimestral, trimestral, cuatrimestral o semestral.
                <br><br>
            </li>
            <li><b>Periodicidad semestral o más frecuente (dependiendo del área temática).</b> La periodicidad o frecuencia de publicación debe estar indicada en la revista y demostrar regularidad en los últimos tres años. La periodicidad es un indicador del flujo de producción científica de la revista, mismo que depende del área temática. Se requiere que las revistas de ciencias sociales y humanidades publiquen al menos dos números al año y, en otras disciplinas, de tres o más. No se aceptarán revistas de periodicidad anual. Se indica a continuación la periodicidad mínima deseada para las revistas según el área temática:
                <br><br>
                <ul style="margin-left: 25px">
                    <li>
                        <i class="icofont icofont-stylish-right text-danger"></i>Ciencias biológicas y médicas → 4 números por año (trimestral)
                        <br><br>
                    </li>
                    <li>
                        <i class="icofont icofont-stylish-right text-danger"></i>Ciencias Exactas, Agrarias, Ingeniería, Tecnología y otras ciencias → 3 números por año (cuatrimestral)
                        <br><br>
                    </li>
                    <li>
                        <i class="icofont icofont-stylish-right text-danger"></i>Ciencias sociales y humanidades → 2 números por año (semestral)
                        <br><br>
                    </li>
                </ul>            
            </li>
            <li>
                <b>Cumplimiento de periodicidad.</b> La revista debe editar al año la cantidad de fascículos o números correspondientes a la periodicidad establecida por la misma revista. La publicación de dos números juntos (volúmenes, fascículos o números) será evaluada negativamente (ejemplo: volumen o número 1/2) y se contabilizará como uno solo. La edición adicional de suplementos o números especiales no serán contabilizados como parte de la periodicidad establecida por la revista. Nota importante: SciELO-México no acepta periodos de embargo para la publicación electrónica de las revistas.
                <br><br>
            </li>
            <li>
                <b>Existencia de tabla de contenido o índice.</b> Preferentemente, las tablas de contenido o índices de la revista deben ordenar los documentos publicados en la revista de acuerdo con una clasificación por tipo de documento (editorial, artículo original, reseñas, etc.).
                <br><br>
            </li>
            <li>
                <b>Identificación de los autores personales o institucionales en los documentos.</b> Los artículos deben estar firmados por los autores personales o, en su caso, por la institución responsable.
                <br><br>
            </li>
            <li>
                <b>Inclusión de afiliación institucional de los autores.</b> Los documentos deben incluir el nombre de la(s) institución(es) a la(s) que está(n) afiliado(s) el autor o los autores. Es recomendable incluir la adscripción del autor con ciudad y país, así como la dirección de correo electrónico de contacto. Estos datos deben incluirse al inicio del artículo.
                <br><br>
            </li>
            <li>
                <b>Carácter científico.</b> Sólo se aceptarán publicaciones con un porcentaje de artículos originales resultado de proyectos de investigación igual o superior al 75% del total de documentos publicados. Otro tipo de documentos como: artículos de revisión, comunicaciones breves, estudios de caso, reseñas, notas, etc., no serán considerados como artículos originales.
                <br><br>
            </li>
            <li>
                <b>Información sobre los servicios de indización que cubren la revista.</b> La revista debe hacer mención de los servicios de indización, resúmenes, directorios o bases de datos en los que está registrada.
                <br><br>
            </li>
            <li>
                <b>Inclusión de normas para la elaboración de citas y referencias bibliográficas.</b> En las instrucciones a los autores deben indicarse las normas de elaboración de citas y referencias bibliográficas.
                <br><br>
            </li>
            <li>
                <b>Fechas de recepción y aceptación de originales.</b> Consignar en los artículos originales la fecha en la que el editor de la revista recibió el artículo propuesto para su publicación, y la fecha en la que se asumió la decisión de aprobar dicho artículo.
                <br><br>
            </li>
            <li>
                <b>Resumen del documento en dos idiomas.</b> Todos los artículos deben incluir resúmenes en el idioma original del trabajo y en un segundo idioma.
                <br><br>
            </li>
            <li>
                <b>Palabras clave en dos idiomas.</b> Inclusión, junto con el resumen en un segundo idioma, de palabras clave o descriptores del documento en el segundo idioma empleado.
                <br><br>
            </li>
        </ol>
    </li>
    <li>
        <i>Evaluación de contenido (calidad científica) de la revista</i><br><br>
        En esta fase de calificación se consideran aspectos que inciden en el rigor y los criterios de selección y evaluación que adopta una revista para la publicación de sus contenidos:<br><br>
        <ol>
            <li>
                <b>Arbitraje por pares.</b> Sólo se aceptarán revistas arbitradas, entendiendo por arbitraje la evaluación por pares. Una revista arbitrada es aquella que somete sus artículos a la revisión de expertos en la disciplina o tema que cubre el artículo. Cada artículo original debe ser examinado al menos por dos especialistas, externos a la institución que edita la revista y al comité editorial.<br><br>
                La revista debe especificar formalmente cuál es el procedimiento seguido para la aprobación de artículos. Asimismo, es obligatoria la indicación en cada uno de los artículos publicados de las fechas de recepción y de aceptación de los artículos.
                <br><br>
            </li>
            <li>
                <b>Autores externos.</b> La revista debe incluir entre 75% y 80% de autores externos a la institución que la edita y a su comité editorial. En el caso de las revistas editadas por asociaciones o sociedades científicas, no se considerarán autores externos aquellos que forman parte de la directiva de la sociedad o figuran en el equipo editorial de la revista.
                <br><br>
            </li>
            <li>
                <b>Conformación del consejo o comité editorial.</b> Se entiende por dicho comité al grupo de especialistas, académicos o científicos en la(s) disciplina(s) que cubre la revista y que se encargan de proponer y asesorar al editor para un mejoramiento continuo de la publicación y/o en la toma de decisiones.
                <br><br>
                La composición del comité editorial de la revista debe ser pública; sus integrantes deben ser especialistas de reconocido prestigio científico, de origen nacional y extranjero, y debidamente identificados en la revista, con sus nombres completos y afiliaciones (nombre completo, institución, ciudad y país). Al menos dos terceras partes del comité editorial deben ser ajenos a la entidad editora de la revista (60-70%).
                <br><br>
                En principio, no serán admitidas revistas que poseen un comité editorial con integrantes que pertenecen prioritariamente a la institución editora y/o con artículos provenientes en su mayor parte de una única institución o región geográfica.
                <br><br>
                En el caso de las revistas editadas por sociedades científicas, el comité editorial no debe estar exclusivamente integrado por miembros que formen parte de la directiva de la sociedad.
                <br><br>
                El objetivo de los puntos 2 y 3 es evaluar el grado de endogamia de una revista. Se entiende por endogamia la concentración de la composición del comité editorial, autores y revisores en miembros afiliados a la institución que edita la revista, a una única institución o lugar geográfico. La endogamia será evaluada negativamente para la admisión de la revista en SciELO-México.
                <br><br>
            </li>
            <li>
                <b>Citas recibidas.</b> Se tomará como elemento para la evaluación de la revista postulante el número de citas recibidas. Para tal efecto, se recurrirá a los indicadores de citación proporcionados por SciELO-México.
                <br><br>
            </li>
            <li>
                <b>Evaluación de especialistas.</b> Una vez que el comité de evaluación SciELO-México ha recibido la postulación de una revista y ha evaluado los puntos anteriormente descritos, recurrirá a una evaluación realizada por especialistas en el área del conocimiento de la revista. La evaluación solicitada a los especialistas estará referida a la calidad científica de los artículos y la importancia de la revista para el desarrollo de la disciplina. Los evaluadores seleccionados no deberán pertenecer a la institución editora de la revista ni formar parte del comité editorial. Por otra parte, los evaluadores serán seleccionados considerando a los investigadores pertenecientes al Sistema Nacional de Investigadores (SNI).
                <br><br>
            </li>
        </ol>
    </li>
    <li>
        Evaluación de la sustentabilidad técnica y financiera para la edición electrónica de la revista en la colección SciELO-México
        <br><br>
        En este rubro se consideran los aspectos técnico-editoriales y el financiamiento para la realización del proceso editorial de acuerdo con la metodología SciELO. SciELO-México ofrece las siguientes opciones para la edición electrónica de revistas en su plataforma:
        <br><br>
        <ul style="margin-left: 25px">
            <li>
                <i>Opción 1: edición descentralizada</i>
                <br><br>
                De acuerdo con esta opción, el editor asume la labor de metaetiquetación y publicación de acuerdo con la metodología de SciELO Publishing Schema.
                <br><br>
                La DGB-UNAM asume los siguientes compromisos:
                <br><br>
                <ul style="margin-left: 25px">
                    <li>
                        <i class="icofont icofont-stylish-right text-danger"></i>Capacitación y supervisión gratuita para editores
                        <br><br>
                    </li>
                    <li>
                        <i class="icofont icofont-stylish-right text-danger"></i>Mediante la contratación de los servicios editoriales de empresas particulares o profesionistas certificados en la edición XML con metodología SciELO Publishing Schema. (Ver lista de empresas y profesionistas certificados y procedimiento para la Certificación de servicios de marcación de textos de acuerdo con SciELO Publishing Schema)
                        <br><br>
                    </li>
                    <li>
                        <i class="icofont icofont-stylish-right text-danger"></i>Soporte informático para la incorporación y preservación de la revista en el portal SciELO-México
                        <br><br>
                    </li>
                </ul>
            </li>
            <li>
                <i>Opción 2: edición centralizada</i>
                <br><br>
                De acuerdo con esta opción, la DGB-UNAM asume el proceso editorial completo de la revista para su incorporación en el portal SciELO-México (metaetiquetación y publicación de acuerdo con la metodología de SciELO Publishing Schema) así como el soporte informático para la incorporación y preservación de la revista en el portal SciELO-México. La cotización de la edición anual de una revista en SciELO-México está en función del número de fascículos y artículos publicados al año por la revista. La cotización promedio es de $500.00 (quinientos pesos) por artículo (más impuestos), de manera que un fascículo con 10 artículos cuesta $5,000.00 y, en caso de tratarse de una revista trimestral, la publicación electrónica en SciELO-México tendría un costo de $20,000.00 al año (más impuestos).
            </li>
        </ul>
    </li>
</ol>
