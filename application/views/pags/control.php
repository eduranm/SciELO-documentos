En general, todos los criterios considerados para la evaluación de revistas y su aceptación se aplican para la permanencia de la revista en la colección, además de los siguientes indicadores:
<br>
<br>
<ol>
    <li>
        <b>Puntualidad en el envío de fascículos publicados.</b> El envío de los fascículos de la revista a SciELO-México debe ser puntual de acuerdo con la periodicidad de la publicación y en el formato requerido (HTML, PDF, InDesign). En su caso, SciELO-México indicará la recurrencia de atrasos en el envío de los fascículos.
        <br><br>
    </li>
    <li>
        <b>Indicador de uso de la revista.</b> El uso de la revista es medido por la evolución mensual del número de accesos o visitas al contenido de la revista. Si el uso de la revista es sistemáticamente bajo y/o decreciente en comparación con revistas de la misma área, la revista deberá ser evaluada para que se analicen las causas y posibles soluciones.
        <br><br>
    </li>
    <li>
        <b>Indicador de impacto.</b> El indicador de impacto de cada título de revista, medido con base en las citas que la revista recibió, debe ser evaluado en conjunto con los títulos de revistas de la misma área.
        <br><br>
    </li>
    <li>
        <b>Exclusión de revistas.</b> La exclusión de una revista de la colección SciELO-México se realizará con base al incumplimiento de los criterios establecidos para la selección y evaluación de revistas, así como con los indicadores de uso e impacto mencionados. En caso de que se verifiquen resultados negativos en la evaluación de desempeño, la revista recibirá una notificación sobre los aspectos que deben mejorarse, y deberán ser resueltos en el plazo establecido en el informe.
        <br>
        <br>
        El editor de la revista podrá en cualquier momento apelar sobre informes recibidos, tanto en los casos de no admisión como en aquellos de solicitud de mejoras para su permanencia en la colección SciELO-México. Por su parte, el comité de selección SciELO-México asume el compromiso de reevaluar el dictamen sobre una revista.
        <br>
        <br>
    </li>
</ol>